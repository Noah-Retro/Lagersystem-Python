// Funktion zum Öffnen des Menüs
function openMenu() {
    document.getElementById("sideMenu").style.width = "250px"; // Menü von links hereinschieben
}

// Funktion zum Schließen des Menüs
function closeMenu() {
    document.getElementById("sideMenu").style.width = "0"; // Menü wieder ausblenden
}
